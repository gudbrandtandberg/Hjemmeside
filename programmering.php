<h3>Solar System II</h3>
<p>
	Soon after finishing my first solar system project, I had the opportunity to do it all again, better, faster, stronger, for a computational physics course I was taking at school. This time, I wrote the program in C++. I solved the same system of equations as in the previous version, except this time I wrote a adaptive block-timestep solver, and used OpenMP to parallelize the program. Finally, I got my first peek at 3D-graphics using OpenGL to visualize the results. Lot's of fun! <a href="https://github.com/gudbrandtandberg/N-Body">Here's</a> the project.
</p>

