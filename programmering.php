<h3>Raspberry Pi Alarm Clock</h3>
<p>
The Raspberry Pi is such a cool little device. I ordered one as soon the first version hit the shelves. Through many iterations of trial-and-error, I ended up with a very cool alarm clock. The clock is operated using three buttons and gives feedback using LED-lights. At the preprogrammed time of awakening, the computer chooses a random song from a list of songs that I like waking up to. Always wake up smiling with this little thing. 
</p>
