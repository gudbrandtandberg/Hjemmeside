<h3>Stegosaur</h3>

<p>
I find image <a href="https://en.wikipedia.org/wiki/Steganography">stegenography</a> to be a fascinating concept! I wrote a steganography program last year, while stranded on a paradise island in Malaysia with a broken leg. It takes as input an image and a message and 'hides' the message in the sequence of least significant bits of the image. <a href="https://github.com/gudbrandtandberg/Stegosaur">Github link</a>.
</p>

